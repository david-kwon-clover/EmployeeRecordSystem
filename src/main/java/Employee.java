public class Employee {
  int employeeId;
  String name;
  int age;
  String jobTitle;
  int salary;

  Employee(int employeeId, String name, int age, String jobTitle, int salary) {
    this.employeeId = employeeId;
    this.name = name;
    this.age = age;
    this.jobTitle = jobTitle;
    this.salary = salary;
  }
}
